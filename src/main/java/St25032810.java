public class St25032810 {
    public static void main(String[] args){
        String ss = "First Java를 공부 중입니다";
        String var1, var2;

        var1 = ss.toUpperCase();
        // ss를 대문자로 바꾸고 var1에 저장
        System.out.println(var1);
        // var1 출력
        var2 = ss.toLowerCase();
        // ss를 소문자로 바꾸고 var2에 저장
        System.out.println(var2);
        // var2 출력
    }
}
