package StudyClass;

public class St25041002 {
    public static void main(String[] args) {

        for (int i = 2; i < 9; i++) { // 2단부터 9단까지

            for (int j = 1; j <= 9; j++) {
                System.out.print(i + "x" + j + "=" + i * j + "  ");
            }
            System.out.println();
        }
    }
}
